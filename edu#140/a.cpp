#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);
  freopen("out.txt", "w", stdout);

	#endif


	ll t; cin >> t;
	while(t--){
		vector<pair<ll, ll>> v;
		for(int i = 0; i< 3; i++){
			ll x, y; cin >> x >> y;
			v.push_back({x, y});
		}
		sort(all(v));
		if(v[0].first != v[1].first && v[1].first != v[2].first && v[2].first != v[0].first){
			cout << "YES\n";
		}
		else if(v[0].second != v[1].second && v[1].second != v[2].second && v[2].second != v[0].second)
			cout << "NO\n";
		else 
			cout << "NO\n";
	}


	return 0;
}