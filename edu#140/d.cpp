#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);

	#endif

	int n; cin >> n;
	string s;
	cin >> s;

	ll mn = 1, mx = (1<<n);
	ll ind = 0;
	ll indmx = 0;

	for(ll i = 1; i <= n; i++){
		if(s[i-1] == '0'){
			mx -= (1<<indmx);
			indmx++;
		}
		else {
			ind++;
			mn = (1<<ind);
		}
	}
	for(ll i = mn; i<= mx; i++)
		cout << i <<  ' ';
	cout << "\n";

	return 0;
}