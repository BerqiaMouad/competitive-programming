#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);
	freopen("out.txt", "w", stdout);
	#endif

	ll t; cin >> t;
	while(t--){
		ll n; cin >> n;
		multiset<ll> mst;
		ll val;
		for(ll i = 0; i< n; i++){
			if(i == 0)
				cin >> val;
			else{
				ll x;
				cin >> x;
				mst.insert(x);
			}
		}
		auto it = mst.upper_bound(val);
		while(it != mst.end()){
			ll x = (*it - val + 1) / 2;
			val += x;
			ll temp = *it - x;
			mst.erase(it);
			mst.insert(temp);
			it = mst.upper_bound(val);
		}
		cout << val << "\n";
	}

	return 0;
}