#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);
	freopen("out.txt", "w", stdout);

	#endif

	ll t; cin >> t;
	while(t--){
		ll n, k; cin >> n >> k;
		priority_queue<pair<ll, ll>, vector<pair<ll, ll>>, greater<pair<ll, ll>>> mnH;
	
		vector<pair<ll, ll>> v(n);
		for(int i = 0; i< n; i++){
			cin >> v[i].second;
		}
		for(int i = 0; i < n; i++){
			cin >> v[i].first;
		}
		for(auto it: v){
			mnH.push(it);
		}
		v.clear();
		ll sub = 0;
		while(k > 0 && !mnH.empty()){
			if(mnH.top().second - (sub + k) <= 0){
				mnH.pop();
				continue;
			}
			sub += k;
			k -= mnH.top().first;
		}
		if(!mnH.empty()){
			cout << "NO\n";
		}
		else cout << "YES\n";
	}

	return 0;
}
