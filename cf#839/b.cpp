#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);
	freopen("out.txt", "w", stdout);

	#endif

	ll t; cin >> t;
	while(t--){
		int mn = INT_MAX;
		vector<vector<int>> v;
		vector<vector<int>> vv;
		for(int i = 0; i< 2; i++){
			int a,b; cin >> a >> b;
			v.push_back({a, b});
			vv.push_back({a, b});
		}

		bool ok = true;
		while(true){
			int temp = v[0][1];
			v[0][1] = v[0][0];
			int tempp = v[1][1];
			v[1][1] = temp;
			temp = v[1][0];
			v[1][0] = tempp;
			v[0][0] = temp;
			if(v[0][0] <= v[0][1] && v[0][0] <= v[1][0] && v[1][0] <= v[1][1] && v[0][1] <= v[1][1]){
				break;
			}
			else {
				bool keepgoing = false;
				for(int i = 0; i < 2; i++){
					for(int j = 0; j< 2; j++){
						if(vv[i][j] != v[i][j]){
							keepgoing = true;
							break;
						}
					}
					if(keepgoing)
						break;
				}
				if(!keepgoing){
					ok = false;
					break;
				}
			}
		}
		if(ok)
			cout << "YES\n";
		else
			cout << "NO\n";
	}



	return 0;
}