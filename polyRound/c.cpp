#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);
	freopen("out.txt", "w", stdout);
	#endif

	ll t; cin >> t;
	while(t--){
		ll n; cin >> n;
		string s;
		cin >> s;
		vector<ll> res(n - 1);
		char curr = s[0];
		ll cnt = 0;
		for(ll i = 2; i<= n; i++){
			res[i - 2] = i - 1;
			if(i > 2){
				if(s[i - 2] == s[i - 3]){
					cnt++;
				}
				else {
					cnt = 0;
				}
				res[i - 2] -= cnt;
			}
		}
		for(auto it : res)
			cout << it << ' ';
		cout << "\n";
	}


	return 0;
}