#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);

	#endif

	ll t; cin >> t;
	while(t--){
		int n; cin >> n;
		string s;
		cin >> s;
		int curr = (s[0] - '0');
		for(int i = 1; i < n; i++){
			if(curr < 0){
				cout << '+';
				curr += (s[i] - '0');
			}
			else {
				cout << '-';
				curr -= (s[i] - '0');
			}
		}
		cout << "\n";
	}


	return 0;
}