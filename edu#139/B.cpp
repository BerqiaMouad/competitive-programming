#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);

	#endif

	ll t; cin >> t;
	while(t--){
		ll n; cin >> n;
		string s; 
		cin >> s;
		map<string, ll> mp;
		if(n == 1)
			cout << "NO\n";
		else {
			bool ok = false;

			for(int i = 0; i < n - 1; i++){
				string temp = ""; temp += s[i]; temp += s[i+1];
				
				mp[temp]++;

				if(mp[temp] > 1){
					ok = true;
					break;
				}
				if(i < n - 2){
					string bla = "";
					bla += s[i+1]; bla += s[i + 2];
					if(temp == bla)
						i++;
				}
			}
			if(ok)
				cout << "YES\n";
			else 
				 cout << "NO\n";
		}

	}

	return 0;
}