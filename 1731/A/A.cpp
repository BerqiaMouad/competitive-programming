#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	ll t; cin >> t;
	while (t--) {
		ll n ; cin >> n;
		vector<ll> a(n);
		for(auto &it: a)
			cin >> it;
		for(int i = 0; i < n ;i++){
			for(int j = i+1; j < n; j++){
				ll temp = a[i] * a[j];
				if(a[i] + a[j] < temp + 1){
					a[j] = 1, a[i] = temp;
				}
			}
		}
		ll s = 0;
		for(auto it: a)
			s += it;
		cout << s * 2022LL << "\n";
	}

	return 0;
}
