#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);

	#endif

	ll t; cin >> t;
	while(t--){
		int n ;cin >> n;
		vector<ll> a(n);
		for(auto &it: a)
			cin >> it;
		ll s = 0;
		for(auto it: a)
			s += it;

		if(s % 2 == 0){
			cout << "0\n";
		}
		else {
			ll cnt = 0;
			ll res = LLONG_MAX;
			for(ll i = 0; i< n; i++){
				ll temps = s - a[i];
				while((temps + a[i]) % 2 && a[i] > 0){
					cnt++;
					a[i] /= 2;
				}
				if((temps + a[i]) % 2 == 0)
					res = min(res, cnt);
				cnt = 0;
			}
			cout << res << "\n";
		}
	}


	return 0;
}