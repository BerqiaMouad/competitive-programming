#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define all(c) c.begin(), c.end()


int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);

	#ifndef ONLINE_JUDGE

	freopen("in.txt", "r", stdin);

	#endif

	ll t; cin >> t;
	while(t--){
		ll n; 
		cin >> n;
		vector<ll> a(n);
		for(auto& it: a)
			cin >> it;
		
		vector<pair<ll, ll>> p;

		for(ll i = 0; i < n; i++){
			ll temp = 1;
			while(temp < a[i]){
				temp *= 2;
			}
			p.push_back({i+1, temp - a[i]});
		}
		cout << p.size() << "\n";
		for(auto it: p)
			cout << it.first << ' ' << it.second << "\n";
	}

	return 0;
}